    def __new__(cls):
        if cls._instance is None:
            cls._instance = super(SBoxManager, cls).__new__(cls)
            cls._instance._init()
        return cls._instance

    def _init(self):
        self.config_path = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), "sboxes.json")
        self.sboxes = self._load_sboxes()

    def _load_sboxes(self) -> Dict[str, List[int]]:
        """加载S盒配置"""
        sboxes = {
            "Standard SM4": self.STANDARD_SBOX,
            "Standard AES": self.STANDARD_AES_SBOX,
            "Standard RC4": self.STANDARD_RC4_SBOX,
            "Standard DES": self.STANDARD_DES_SBOX
        }
        if os.path.exists(self.config_path):
            try:
                with open(self.config_path, 'r', encoding='utf-8') as f:
                    custom_sboxes = json.load(f)
                    sboxes.update(custom_sboxes)
            except Exception as e:
                print(f"Failed to load custom S-Boxes: {e}")
        return sboxes

    def save_sboxes(self):
        """保存自定义S盒"""
        custom_sboxes = {k: v for k, v in self.sboxes.items() if k not in ["Standard SM4", "Standard AES", "Standard RC4", "Standard DES"]}
        try:
            with open(self.config_path, 'w', encoding='utf-8') as f:
                json.dump(custom_sboxes, f, indent=4)
        except Exception as e:
            print(f"Failed to save custom S-Boxes: {e}")

    def get_all_names(self) -> List[str]:
        """获取所有S盒名称"""
        return list(self.sboxes.keys())

    def get_sbox(self, name: str) -> List[int]:
        """获取指定S盒"""
        # Lazy reload check: If name is custom but not in memory, try reload?
        if name not in self.sboxes and name not in ["Standard SM4", "Standard AES", "Standard RC4", "Standard DES"]:
             self._init() # Reload config
             
        return self.sboxes.get(name, self.STANDARD_SBOX)

    def add_sbox(self, name: str, sbox: List[int]) -> bool:
        """添加S盒"""
        if name in self.sboxes and name in ["Standard SM4", "Standard AES", "Standard RC4", "Standard DES"]:
            return False # Cannot overwrite standard
        self.sboxes[name] = sbox
        self.save_sboxes()
        return True

    def remove_sbox(self, name: str) -> bool:
        """删除S盒"""
        if name in ["Standard SM4", "Standard AES", "Standard RC4", "Standard DES"]:
            return False
        if name in self.sboxes:
            del self.sboxes[name]
            self.save_sboxes()
            return True
        return False
