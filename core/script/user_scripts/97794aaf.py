# ==========================================
# 3. SM4 算法实现 (修正版)
# ==========================================
def rotl(x, n):
    return ((x << n) & 0xffffffff) | ((x >> (32 - n)) & 0xffffffff)

def non_linear_transform_swapped(b):
    # 【关键修正】：sub_125E 和 sub_1311 中的特殊逻辑
    # 输入的 32位整数 b (Big Endian 视角: B3 B2 B1 B0)
    # 代码逻辑：v4 = S(B0), v3 包含 S(B1)..S(B3)
    # 返回: (v4 << 24) | v3
    # 结果: S(B0) S(B1) S(B2) S(B3)
    # 这导致字节顺序相对于标准 SM4 被反转了
    
    b0 = b & 0xff         # LSB
    b1 = (b >> 8) & 0xff
    b2 = (b >> 16) & 0xff
    b3 = (b >> 24) & 0xff # MSB
    
    return (CUSTOM_SBOX[b0] << 24) | \
           (CUSTOM_SBOX[b1] << 16) | \
           (CUSTOM_SBOX[b2] << 8) | \
           (CUSTOM_SBOX[b3])

def l_transform(b):
    return b ^ rotl(b, 2) ^ rotl(b, 10) ^ rotl(b, 18) ^ rotl(b, 24)

def l_transform_key(b):
    return b ^ rotl(b, 13) ^ rotl(b, 23)

def round_function(xi, xi1, xi2, xi3, rk):
    # 使用修正后的 S-Box 变换
    return xi ^ l_transform(non_linear_transform_swapped(xi1 ^ xi2 ^ xi3 ^ rk))

