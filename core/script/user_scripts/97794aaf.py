def key_expansion(key):
    rk = []
    k = [0] * 4
    mk = struct.unpack('>4I', key)
    k[0] = mk[0] ^ FK[0]
    k[1] = mk[1] ^ FK[1]
    k[2] = mk[2] ^ FK[2]
    k[3] = mk[3] ^ FK[3]
    for i in range(32):
        temp = k[i+1] ^ k[i+2] ^ k[i+3] ^ CK[i]
        # 注意：密钥扩展也调用了 sub_1311，它同样使用了反转逻辑
        b = non_linear_transform_swapped(temp)
        rk_val = k[i] ^ l_transform_key(b)
        rk.append(rk_val)
        k.append(rk_val)
    return rk

def sm4_decrypt_ecb(data, key):
    rk = key_expansion(key)
    rk = rk[::-1] # 解密逆序
    
    output = b''
    for i in range(0, len(data), 16):
        block = data[i:i+16]
        if len(block) < 16: break
        
        x = list(struct.unpack('>4I', block))
        for r in range(32):
            x.append(round_function(x[r], x[r+1], x[r+2], x[r+3], rk[r]))
            
        output += struct.pack('>4I', x[35], x[34], x[33], x[32])
    
    # 去除 Padding
    pad_len = output[-1]
    if 0 < pad_len <= 16:
        output = output[:-pad_len]
    return output

